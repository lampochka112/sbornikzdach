def main():
    # Ввод данных
    k = int(input("Введите день года (1-365): "))
    while k < 1 or k > 365:
        print("Ошибка: день года должен быть от 1 до 365")
        k = int(input("Введите день года (1-365): "))

    print("\nа) Если 1 января - понедельник:")
    n_a = (k - 1) % 7
    print(f"День недели: {n_a} (0-воскресенье, 1-понедельник, ..., 6-суббота)")

    print("\nб) Если 1 января - вторник:")
    n_b = k % 7
    print(f"День недели: {n_b} (0-воскресенье, 1-понедельник, ..., 6-суббота)")

    print("\nв*) Если 1 января - произвольный день недели (1-пн, ..., 7-вс):")
    d = int(input("Введите день недели 1 января (1-7): "))
    while d < 1 or d > 7:
        print("Ошибка: день недели должен быть от 1 до 7")
        d = int(input("Введите день недели 1 января (1-7): "))
    
    n_c = (k + d - 2) % 7
    print(f"День недели: {n_c} (0-воскресенье, 1-понедельник, ..., 6-суббота)")

if __name__ == "__main__":
    main()