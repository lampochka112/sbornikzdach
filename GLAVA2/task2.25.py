n = int(input("Введите число n (1 ≤ n ≤ 999): "))

if n < 1 or n > 999:
    print("Ошибка: число n должно быть от 1 до 999")
else:
    a = n % 10

    bc = (n - a) // 10

    x = a * 100 + bc

    if (x % 100) * 10 + (x // 100) == n:
        print(f"Искомое число x: {x}")
    else:
        print("Для данного n не существует трехзначного x, удовлетворяющего условию")