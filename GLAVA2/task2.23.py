n = int(input("Введите число n (10 ≤ n ≤ 999, десятки ≠ 0): "))

if n < 10 or n > 999 or (n // 10) % 10 == 0:
    print("Ошибка ввода: n должно быть от 10 до 999, и десятки не должны быть нулём.")
else:
    c = n // 100  
    ab = n % 100   
    x = ab * 10 + c

    if (x - (x % 10)) // 10 == ab and int(f"{x % 10}{ab}") == n:
        print(f"Искомое число x: {x}")
    else:
        print("Не удалось найти x, соответствующее условиям.")