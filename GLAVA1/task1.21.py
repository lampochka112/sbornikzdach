try:
    C = float(input("Температура в °C: "))
    
    if C < -273.15:
        print("Ошибка: температура ниже абсолютного нуля!")
    else:
        F = C * 9/5 + 32  
        K = C + 273.15
        print(f"{C}°C = {F:.1f}°F = {K:.2f} K")
        
except ValueError:
    print("Ошибка! Введите числовое значение температуры.")